//JAVA program to print a pattern of square using  star

import java.util.*;
public class J2 {
    public static void main(String[] args) {
        int i, j, k=5;
        for(i=1; i<=k; i++){
            for (j=1; j<=k; j++){
                System.out.print("* ");
            }
            System.out.println(" ");
        }
    }

}
